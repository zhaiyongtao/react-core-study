function test (...arg) {
    console.log(arg) //
}

test('123','456','789') // [ '123', '456', '789' ]

test`123;456;469;` // [ [ '123;456;469;' ] ]

// 如果我们在调用的时候插入其他的变量：
// 模板字符串被拆分了；
// 第一个元素是数组，是被模块字符串拆分的字符串组合；
// 后面的元素是一个个模块字符串传入的内容；
const name = 'zyt'
test`123,${name},456` // [ [ '123,', ',456' ], 'zyt' ]

